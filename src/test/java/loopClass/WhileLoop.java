package loopClass;

public class WhileLoop {

	public static void main(String[] args) {

		// While - We use when we dont know up till, we need to go - 
		
		// Example
		// int i = 1;
		// while (true)
		//{
		//i++;
		//} end of while loop 	
		
		int i = 1, j = 1, k = 1;

		while (i <= 10) {
			System.out.println("I is : " + i * 2);
			i++;
		}
		while (j <= 10) {
			System.out.println("I is : " + j * 3);
			j++;
		}

		while (k <= 10) {
			System.out.println("I is : " + k * 4);
			k++;
		}

	}

}
